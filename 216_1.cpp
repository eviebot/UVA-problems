#include <bits/stdc++.h>
using namespace std;

#define endl '\n'
#define pb push_back
#define pf push_front
#define fi first
#define se second
#define MP make_pair

typedef long long int lli;
ofstream outfile;
ifstream infile;


vector <pair <int, int>> coords;
vector <int> permute;
double dist(int a, int b)
{
    int x = coords[a].fi - coords[b].fi, y = coords[a].se - coords[b].se;
    return sqrt(x*x + y*y);
}

int main()
{
    infile.open("input.txt");
    outfile.open("output.txt");

    //ios::sync_with_stdio(false);cout.tie(0);cin.tie(0);

    int n, i, v1, v2, c = 1;
    double sum , mini = 1e10;
    vector <int> fin;
    cout << fixed << setprecision(2);
    while(cin >> n)
    {
        if( !n)
            break;
        coords.clear();
        permute.clear();
        coords.pb( MP (0, 0));
        permute.pb(0);
        for(i = 1; i<= n; ++i)
        {
            cin >> v1 >> v2;
            coords.pb( MP (v1, v2));
            permute.pb( i );
        }

        mini = 1e10;
        do
        {
            sum = 0;
            //for(i = 1; i<= n; ++i)
                //cout << permute[i] << ' ';
            //cout << '\n';
            for(i = 2; i<= n; ++i)
            {
                sum += dist(permute[i], permute[i-1]);
            }
            if( sum < mini)
            {
                mini = sum;
                fin = permute;
            }
        }while(next_permutation(permute.begin()+1, permute.end()));

        sum = 0;
        cout << "**********************************************************\n";
        cout << "Network #"<< c++<< "\n";
        for(i = 1; i< n; ++i)
        {
            cout << "Cable requirement to connect ("<< coords[fin[i]].fi<<','<< coords[fin[i]].se<<") to (";
            cout << coords[fin[i+1]].fi<< ','<< coords[fin[i+1]].se<<") is "<<(dist(fin[i], fin[i+1])) + 16<<" feet.\n";
            sum += dist(fin[i], fin[i+1]) + 16;
        }
        cout << "Number of feet of cable required is "<< sum << ".\n";
    }
}
