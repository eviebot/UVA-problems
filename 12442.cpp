#include <bits/stdc++.h>
using namespace std;

#define endl '\n'
#define pb push_back
#define pf push_front
#define fi first
#define se second
#define MP make_pair

typedef long long int lli;
ofstream outfile;
ifstream infile;

int n;
vector <bool> visited;
vector <int> cycle, dist, graph;
int DFS_cycle(int root)
{
    if(visited[root])
    {
        if( !cycle[root])
            return root;
        return -1;
    }
    visited[root] = true;
    int temp = DFS_cycle(graph[root]);
    cycle[root] = temp;
    if(temp > 0)
    {
        if(temp == root)
        {
            return -1;
        }
        return temp;
    }
    else
        return -1;
}

int DFS_prop(int root)
{
    if(cycle[root] > 0)
    {
        return cycle[root];
    }
    dist[root] = DFS_prop(graph[root]) + 1;
    return dist[root];
}
int main()
{
    infile.open("input.txt");
    outfile.open("output.txt");

    //ios::sync_with_stdio(false);cout.tie(0);cin.tie(0);

    int T, i, u, v;
    infile >> T;
    for(int z = 1; z <= T; ++z)
    {
        infile >> n;
        visited.clear();
        dist.clear();
        cycle.clear();
        for(i =0 ; i <= n; ++i)
        {
            cycle.pb(0);
            dist.pb(0);
            visited.pb(0);
            graph.pb(0);
        }

        for(i = 0; i < n; ++i)
        {
            infile >> u >> v;
            graph[u] = v;
        }

        for(i = 1; i <= n; ++i)
        {
            if( !visited[i])
                DFS_cycle(i);
        }
        for(i = 1; i <= n; ++i)
        {
            if(cycle[i] > 0)
            {
                ++dist[cycle[i]];
            }
        }
        int maxi = 0, max_node;
        for(i = 1; i <= n; ++i)
        {
            if(cycle[i] > 0)
            {
                dist[i] = dist[cycle[i]];
            }

            if( !dist[i] )
            {
                dist[i] = DFS_prop(i);
            }
            if(dist[i] > maxi)
            {
                maxi = dist[i];
                max_node = i;
            }
        }
        cout << "Case "<< z << ": "<< max_node << "\n";
    }
}
