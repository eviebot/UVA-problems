#include <bits/stdc++.h>
using namespace std;

#define endl '\n'
#define pb push_back
#define pf push_front
#define fi first
#define se second
#define MP make_pair

typedef long long int lli;
ofstream outfile;
ifstream infile;

bitset <8> BITS;
int x, y, n;
vector <vector <vector <double>>>  TSP;
vector <vector <vector <int>>> prevv;
vector <double> temp;
vector <vector <double>> ttemp;
vector <vector <int>> ttemp1;
vector <int> temp1;
vector <pair <int, int> >  coords;

double edistance(int a, int b)
{
    int v1 = coords[a].fi - coords[b].fi, v2 = coords[a].se - coords[b].se;
    double d = sqrt( v1 * v1 + v2 * v2 );
    //cout << a << " "<< b << " "<< d << "\n";
    return d;
}
void topdown(bitset <8> & B, int leader, int start)
{
    int sum = 0, val = B.to_ulong(), val1, i, j, p;

    if( TSP[start][val][leader] != -1)
        return ;

    vector <int> candidates;
    bitset <8> C = B;
    //cout << B << " "<< leader << '\n';
    C.flip(leader);
    val1 = C.to_ulong();

    for(i = 0; i< n; ++i)
    {
        if( B[i] )
        {
            ++sum;
            if( i != leader && i != start)
                candidates.pb(i);
        }
    }
    if( sum == 2)
    {
        prevv[start][val][leader] = 0;
        TSP[start][val][leader] = edistance(0, leader);
    }
    else
    {
        double dist = 1000000, s;
        for(j = 0; j < candidates.size() ; ++j)
        {
            s = edistance(leader, candidates[j]);
            topdown(C, candidates[j], start);
            if( (TSP[start][val1][candidates[j]] +  s) < dist )
            {
                p = candidates[j];
                dist = TSP[start][val1][candidates[j]] + s;
            }
        }
        TSP[start][val][leader] = dist;
        prevv[start][val][leader] = p;
    }
}
int main()
{
    infile.open("input.txt");
    outfile.open("output.txt");

    //ios::sync_with_stdio(false);cout.tie(0);cin.tie(0);
    int counter = 1, start, index;
    while(infile >> n)
    {
        if( !n )
            break;
        int i, rt, c[n];
        double mini[n], placeholder;
        coords.clear();
        TSP.clear();
        temp.clear();
        prevv.clear();
        for(i = 0; i < n; ++i)
        {
            //cout << i << " "<< TSP[i] << "\n";
            infile >> x >> y;
            coords.pb( MP(x, y) );

            temp.pb(-1.0);
            temp1.pb(0);
        }
        for(i = 0; i< (int)(pow(2, n)); ++i)
        {
            ttemp1.pb(temp1);
            ttemp.pb(temp);
        }
        for(i =0 ; i< n; ++i)
        {
            TSP.pb(ttemp);
            prevv.pb(ttemp1);
        }

        BITS.reset();
        for(i = 0; i< n; ++i)
        {
            BITS.set(i);
        }
        rt = BITS.to_ulong();

        for(start = 0; start < n; ++start)
        {
            mini[start] = 1000000;
            for(i = 0; i< n; ++i)
            {
                if( i != start)
                {
                    topdown(BITS, i, start);

                    if( TSP[start][rt][i] < mini[start])
                    {
                        //cout << i << " "<< TSP[rt][i] << "\n";
                        mini[start] = TSP[start][rt][i];
                        c[start] = i;

                    }
                }
            }
        }
        placeholder = 1000000;

        for(i = 0; i< n; ++i)
        {
            cout << mini[i] << "\n";
            if( mini[i] < placeholder)
            {
                index = c[i];
                placeholder = mini[i];
            }
        }
        cout << index << '\n';
        vector <pair <int, int>> pairing;

        int d, cc = index;
        while(cc)
        {
            pairing.pb( MP (prevv[index][BITS.to_ulong()][cc], cc ) );
            d = cc;
            cc = prevv[index][BITS.to_ulong()][cc];
            BITS.flip(d);
        }


        double sum = 0;
        cout <<  fixed <<setprecision(2);
        cout << "**********************************************************\n";
        cout << "Network #"<< counter++<<"\n";
        for(d = pairing.size()-1; d >= 0; --d)
        {
            cout << "Cable requirement to connect ("<< coords[pairing[d].fi].fi<<","<< coords[pairing[d].fi].se<<") to ("<< coords[pairing[d].se].fi <<","<<coords[pairing[d].se].se<< ") is ";
            cout <<  edistance(pairing[d].fi, pairing[d].se) + 16<<" feet.\n";
            sum += edistance(pairing[d].fi, pairing[d].se) + 16;
        }
        cout << "Number of feet of cable required is "<< sum << ".\n";

    }
}
