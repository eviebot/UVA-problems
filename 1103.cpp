#include <bits/stdc++.h>
using namespace std;

#define endl '\n'
#define pb push_back
#define pf push_front
#define fi first
#define se second
#define MP make_pair

typedef long long int lli;
ofstream outfile;
ifstream infile;

int H, W, cycles, arr[6];
bool graph[200][200];
bool visited[200][200];

void DFS(int row, int col)
{
    if( row >= H || row < 0 || col < 0 || col >= W*4)
        return;
    if( visited[row][col])
    {
        ++cycles;
        return;
    }
    visited[row][col] = true;
    DFS(row+1, col);
    DFS(row-1, col);
    DFS(row, col+1);
    DFS(row, col-1);
}

int main()
{
    infile.open("input.txt");
    outfile.open("output.txt");

    //ios::sync_with_stdio(false);cout.tie(0);cin.tie(0);

    int i, j, k, t;
    char c;
    bitset <4> converter;
    while(infile >> H >> W)
    {
        if(!H)
            break;
        for(j =0 ; j < H; ++j)
        {
            for(i = 0; i < W; ++i)
            {
                infile >> c;
                if( c > 90)
                    converter = c-'a'+10;
                else
                    converter = c-'0';
                t = i*4;
                for(k = 0; k < 4; ++k)
                {
                    visited[j][t+k] = 0;
                    graph[j][t + k] = converter[3-k];
                }
            }
        }
        for(i =0 ; i < H; ++i)
        {
            for(j =0 ; j < W; ++j)
            {
                t = j*4;
                for(k = 0; k < 4; ++k)
                {
                    cout << graph[i][t+k];
                }

            }
            cout << "\n";
        }
        for(i =0; i < 6; ++i)
        {
            arr[i] = 0;
        }
        for(i =0 ; i < H; ++i)
        {
            for(j =0 ; j < W; ++j)
            {
                t = j*4;
                for(k = 0; k < 4; ++k)
                {
                    if(!visited[i][t+k] && graph[i][t+k])
                    {
                        cycles = 0;
                        DFS(i, t+k);
                        ++arr[cycles];
                    }
                }
            }
        }
        for(i =0 ; i < 6; ++i)
            cout << arr[i] << '\n';
    }
}
