#include <bits/stdc++.h>
using namespace std;

#define endl '\n'
#define pb push_back
#define pf push_front
#define fi first
#define se second
#define MP make_pair

typedef long long int lli;
ofstream outfile;
ifstream infile;

int main()
{
    infile.open("input.txt");
    outfile.open("output.txt");

    ios::sync_with_stdio(false);cout.tie(0);cin.tie(0);


    int n, m, emod, v1, v2, i;

    cin >> n;

    for(int z = 0; z < n; ++z)
    {
        cin >> m >> emod;
        vector <pair <int, int> > data;
        data.pb( MP (0, 0));
        for(i = 0; i< m; ++i)
        {
            cin >> v1 >> v2;

            data.pb( MP (v1, v2));
        }

        int DP[emod+1][emod+1];

        memset(DP , -1, sizeof(DP));
        DP[0][0] = 0;
        for(i = 1; i< data.size(); ++i)
        {
            if( data[i].fi == 0)
            {
                for( v1 = 0; v1 <= emod; ++v1 )
                {
                    for(v2 = data[i].se; v2 <= emod ; ++v2)
                    {
                        if( DP[v1][v2-data[i].se] != -1 && DP[v1][v2-data[i].se] < (DP[v1][v2] >0 ?DP[v1][v2]: 1e9))
                            DP[v1][v2] = DP[v1][v2-data[i].se] + 1;
                    }
                }
            }
            else if( data[i].se == 0)
            {
                for(v2 = 0; v2 <= emod; ++v2)
                {
                    for(v1 = data[i].fi; v1 <= emod; ++v1)
                    {
                        if( DP[v1-data[i].fi][v2] != -1 && DP[v1-data[i].fi][v2] < (DP[v1][v2] >0 ?DP[v1][v2]: 1e9))
                            DP[v1][v2] = DP[v1-data[i].fi][v2] + 1;
                    }
                }
            }
            else
            {
                for(v1 = data[i].fi; v1 <= emod; ++v1)
                {
                    for(v2 = data[i].se; v2 <= emod; ++v2)
                    {
                        if( DP[v1-data[i].fi][v2-data[i].se] != -1 && DP[v1-data[i].fi][v2-data[i].se] < (DP[v1][v2] >0 ?DP[v1][v2]: 1e9))
                            DP[v1][v2] = DP[v1-data[i].fi][v2-data[i].se] + 1;
                    }
                }
            }
            //cout << DP[300][0] << "\n";
        }


        int ubound = emod / 1.414 +1, mini = 1e9, t;
        double x;
        for(i = 0; i< ubound; ++i)
        {
            x = sqrt(emod*emod - i*i);
            t = (int)(x);
            //cout << x << " "<< t  << " "<< i << "\n";
            if( x == t)
            {
                if( DP[i][t] != -1 && DP[i][t] < mini)
                    mini = DP[i][t];
                if( DP[t][i] != -1 && DP[t][i] < mini)
                    mini = DP[t][i];
            }
        }
        if( mini == 1e9)
            cout << "not possible\n";
        else
            cout << mini << "\n";
    }
}
