#define se second
#define MP make_pair

typedef long long int lli;
ofstream outfile;
ifstream infile;

int main()
{
    infile.open("input.txt");
    outfile.open("output.txt");

    //ios::sync_with_stdio(false);cout.tie(0);cin.tie(0);


    long long int N, Q, D, M, sum, temp;
    int c = 1, q_no, i, j, k;
    while(true)
    {
        infile >> N >> Q;
        if( !N && !Q)
            break;

        int data[N+1];
        for(i =1 ; i<= N; ++i)
        {
            infile >> data[i];
        }

        for(q_no = 1; q_no <=  Q; ++q_no)
        {
            infile >> D >> M;

            int mdp[M+1][N+1][D];
            for(i = 0; i<= M; ++i)
            {
                for(k = 0; k<= N; ++k)
                {
                    for(j = 0; j< D; ++j)
                    {
                        mdp[i][k][j] = 0;
                    }
                }
            }

            for( i = 1; i<= M; ++i )
            {
                for(j = 1; j<= N; ++j)
                {
                    if( i > j)
                        continue;

                    for(k = 0; k < D; ++k)
                    {
                        sum = k + data[j];
                        mdp[i-1][j][k] = mdp[i-1][j-1][k];
                        sum = ( (sum % D) + D )%D;
                        mdp[i][j][sum] = mdp[i-1][j-1][k]+1;
                    }
                }
            }

            for(k = 0; k< D; ++k)
            {
                cout << "k = "<< k <<"\n";
                for(i = 1; i<= M; ++i)
                {
                    for(j = 1; j<= N; ++j)
                        cout << mdp[i][j][k]<<' ';
                    cout << '\n';
                }
            }
        }
    }
    return 0;
}
