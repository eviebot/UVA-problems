#include <bits/stdc++.h>
using namespace std;

#define endl '\n'
#define pb push_back
#define pf push_front
#define fi first
#define se second
#define MP make_pair

typedef long long int lli;
ofstream outfile;
ifstream infile;

int n;

vector <vector <int>> graph;
vector <int> temp, discover, parent, low;
vector <bool> visited;
vector <pair <int, int>> myvec;
void find_critical(int vertex, int time)
{
    discover[vertex] = time+1;
    low[vertex] = time+1;
    visited[vertex] = true;
    for(int j = 0; j < graph[vertex].size(); ++j)
    {
        if( !visited[graph[vertex][j]] )
        {
            parent[graph[vertex][j]] = vertex;
            find_critical(graph[vertex][j], time+1);

            low[vertex] = min(low[vertex], low[graph[vertex][j]]);

            if( low[graph[vertex][j]] > discover[vertex] )
            {
                if( vertex < graph[vertex][j])
                    myvec.pb(MP (vertex, graph[vertex][j]));
                else
                    myvec.pb(MP (graph[vertex][j], vertex));
            }

        }
        else if( parent[vertex] != graph[vertex][j])
        {
            low[vertex] = min(low[vertex], discover[graph[vertex][j]] );
        }
    }
}
int main()
{
    infile.open("input.txt");
    outfile.open("output.txt");

    //ios::sync_with_stdio(false);cout.tie(0);cin.tie(0);

    int v, i, j, u;
    char c1, c2;
    while(cin >> n)
    {
        if( !n){
            cout << "0 critical links\n\n";continue;}
        graph.clear();
        visited.clear();
        discover.clear();
        low.clear();
        myvec.clear();
        parent.clear();

        for(i = 0; i < n; ++i)
        {
            graph.pb(temp);
            visited.pb(0);
            low.pb(0);
            parent.pb(-1);
            discover.pb(0);
        }

        for(int z =0; z < n; ++z)
        {
            scanf("%d %c%d%c", &v, &c1, &j, &c2);
            for(i =0 ; i < j; ++i)
            {
                cin >> u;
                graph[v].pb(u);
            }
        }
        for(i = 0; i < n; ++i)
        {
            if(!visited[i])
                find_critical(i, 0);
        }

        sort(myvec.begin(), myvec.end());

        cout << myvec.size() << " critical links\n";
        for(i =0 ; i < myvec.size(); ++i)
        {
            cout << myvec[i].fi << " - "<< myvec[i].se << "\n";
        }
        cout << "\n";
    }
}
