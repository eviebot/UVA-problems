    int i;

    if( (se -fi) % 2 == 0)
    {
        ma = 0;
    }
    for(i = fi+2; i <= se; i+=2 )
    {
        if( (a[i] - a[i-2]) > ma )
            ma = a[i] - a[i-2];
    }
    if( a[se] - a[se-1] > ma)
        ma = a[se] - a[se-1];

