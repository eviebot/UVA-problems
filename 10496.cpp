        coords.pb( MP (v1, v2));

        mybitset.set(0);
        temp.pb(-1);
        for(i = 1; i<= n; ++i)
        {
            temp.pb(-1);
            mybitset.set(i);
            cin >> v1 >> v2;
            coords.pb( MP (v1, v2));
        }
        for(i = 0; i< (int)(pow(2, n+1)); ++i)
        {
            DP.pb(temp);
        }

        int mini = 1e9;
        for(i = 1; i<= n; ++i)
        {

            topdown(mybitset, i);

            if( DP[mybitset.to_ulong()][i] + dist(0, i) < mini)
            {
                //cout << DP[mybitset.to_ulong()][i] << ' ' << i << "\n";
                mini = DP[mybitset.to_ulong()][i] + dist(0, i);
            }
        }
