#include <bits/stdc++.h>
using namespace std;

#define endl '\n'
#define pb push_back
#define pf push_front
#define fi first
#define se second
#define MP make_pair

typedef long long int lli;
ofstream outfile;
ifstream infile;


bitset <11> mybitset;
vector <pair <int, int>> coords;
vector <vector <int>> DP;
vector <int> temp;
int l, b, n;

int dist(int a, int b)
{
    return ( abs(coords[a].fi - coords[b].fi) + abs(coords[a].se - coords[b].se) );
}
void topdown(bitset <11> & myb, int last)
{
    if( DP[myb.to_ulong()][last]  != -1)
        return ;
    bitset <11> C = myb;
    C.flip(last);

    int sum = 0;
    vector <int> candi;
    for(int i = 1; i<= n; ++i)
    {
        if( C[i] )
        {
            ++sum;
            candi.pb(i);
        }
    }
    if( sum == 0)
    {
        DP[myb.to_ulong()][last] = dist(0, last);
    }
    else
    {
        int mini = 1e9;
        for(int j = 0; j< candi.size(); ++j)
        {

            topdown(C, candi[j]);

            if( DP[C.to_ulong()][candi[j]] + dist(candi[j], last) < mini )
                mini = DP[C.to_ulong()][candi[j]] + dist(candi[j], last);
        }
        DP[myb.to_ulong()][last] = mini;
    }
}
