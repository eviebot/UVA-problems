void gcd(int *a, int *b)
{
    int i;
    while(true)
    {
        for(i = *a;i>=1;i--)
        {
            if(*a%i==0 && *b%i==0)
            {
                *a /= i;
                *b /= i;
                break;
            }
        }
        if(i==1)
            break;
    }
}

int digit(int x)
{
    int i=0;
    while(x!=0)
    {
        x/=10;
        i++;
    }
    return i;
}
